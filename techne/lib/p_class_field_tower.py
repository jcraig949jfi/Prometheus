"""TOOL_P_CLASS_FIELD_TOWER — p-Hilbert class field tower.

Extends TOOL_HILBERT_CLASS_FIELD with restriction to a p-Sylow subgroup.

Mathematical background
-----------------------
The Hilbert class field H(K) of a number field K is the maximal unramified
abelian extension; Gal(H/K) ≅ Cl(K) by class field theory. The
**p-Hilbert class field** H_p(K) is the maximal unramified abelian
p-extension of K, with

    Gal(H_p(K) / K) ≅ Cl(K)[p^∞]

(the p-Sylow of Cl(K)). Iterating yields the **p-class-field tower**

    K = K_0 ⊂ K_1 ⊂ K_2 ⊂ ...,    K_{n+1} = H_p(K_n).

By a theorem of Golod and Shafarevich (1964), this tower may be infinite;
when it terminates at depth d, K_d has trivial p-class group and is its
own p-Hilbert class field.

References
----------
* Cohen, "A Course in Computational Algebraic Number Theory", §6.3.4.
* Washington, "Introduction to Cyclotomic Fields", 2nd ed., §10.3.

Implementation
--------------
PARI's `bnrclassfield(bnr, subgroup, flag)` computes the abelian extension
of K cut out by the subgroup `subgroup` of the ray-class group. With
modulus 1 (unramified) the ray-class group is the ideal class group, with
structure `bnf.cyc = [c_1, ..., c_k]`. The p-Sylow subgroup of Cl(K) is
the quotient by the **p'-part subgroup** generated by `(p^{v_p(c_i)})_i`;
passing this subgroup to `bnrclassfield` returns the p-Hilbert class field
in absolute form.

Forged: 2026-04-25 | Project #29 Phase 1 | Tier 1 (cypari wrapper).
"""
from __future__ import annotations

from typing import Optional, Union

import cypari


_pari = cypari.pari

# Default 1.5 GB PARI stack; iterated p-HCF on degree-12 base fields can
# exceed 4 GB at depth 2-3. We auto-grow on overflow up to _MAX_STACK_BYTES.
_DEFAULT_STACK_BYTES = 1_500_000_000
_MAX_STACK_BYTES = 4_000_000_000
try:
    _pari.allocatemem(_DEFAULT_STACK_BYTES)
except cypari._pari.PariError:
    pass


# ---------------------------------------------------------------------------
# Internal helpers
# ---------------------------------------------------------------------------

def _coerce_poly(polynomial, var: str = 'x') -> str:
    """Convert polynomial input to a PARI-parseable string.

    Variable can be swapped to 'y' for use as a base field in a relative
    extension (PARI requires base variables to have lower priority than
    relative variables).
    """
    if isinstance(polynomial, str):
        if var != 'x':
            return polynomial.replace('x', var)
        return polynomial
    coeffs = list(polynomial)
    if not coeffs:
        raise ValueError("polynomial coefficient list is empty")
    deg = len(coeffs) - 1
    terms = []
    for i, c in enumerate(coeffs):
        power = deg - i
        if c == 0:
            continue
        if power == 0:
            terms.append(f"({int(c)})")
        elif power == 1:
            terms.append(f"({int(c)})*{var}")
        else:
            terms.append(f"({int(c)})*{var}^{power}")
    if not terms:
        raise ValueError("polynomial is identically zero")
    return "+".join(terms)


def _safe_call(fn, *, max_retries: int = 3):
    """Run a PARI thunk; on stack overflow, double the stack and retry."""
    last_err: Optional[Exception] = None
    for _ in range(max_retries + 1):
        try:
            return fn()
        except cypari._pari.PariError as e:
            if 'stack overflows' not in str(e):
                raise
            last_err = e
            new_size = min(2 * _pari.stacksize(), _MAX_STACK_BYTES)
            if new_size <= _pari.stacksize():
                break
            _pari.allocatemem(new_size)
    if last_err is not None:
        raise last_err


def _validate_prime(p) -> int:
    """Validate p is a rational prime, return as int."""
    if not isinstance(p, int) or p < 2:
        raise ValueError(f"p must be a rational prime >= 2; got {p!r}")
    if not _pari(f"isprime({p})"):
        raise ValueError(f"p = {p} is not prime")
    return p


def _p_adic_valuation(n: int, p: int) -> int:
    """Largest k with p^k | n. v_p(0) := 0 (only used for cyc entries)."""
    if n == 0:
        return 0
    n = abs(int(n))
    k = 0
    while n % p == 0:
        n //= p
        k += 1
    return k


def _polynomial_degree(pol_str: str) -> int:
    """Return PARI's poldegree of a polynomial string."""
    return int(_pari(f"poldegree({pol_str})"))


def _p_part_subgroup_matrix(cyc: list[int], p: int) -> str:
    """Return PARI subgroup matrix for the p'-part of Cl(K) (kernel of p-Sylow).

    For cyc = [c_1, ..., c_k] with c_i = p^{a_i} * m_i (gcd(m_i, p) = 1),
    the p'-part of Z/c_i is generated by p^{a_i} (an element of order m_i),
    so the p'-part subgroup of (Z/c_1) x ... x (Z/c_k) is generated by
    p^{a_i} * e_i. As a column-matrix in PARI:

        diag(p^{a_1}, ..., p^{a_k})

    Passing this as `subgroup` to `bnrclassfield` returns the field cut
    out by the quotient (Cl(K) / p'-part) = p-Sylow of Cl(K).

    Edge cases:
      * cyc = [] -> matid(0) (empty matrix), trivial class group.
      * any a_i = 0 (p ∤ c_i) -> diagonal entry 1, that factor contributes
        no p-part and the row is fully in the subgroup.
    """
    if not cyc:
        return "matid(0)"
    diag = [p ** _p_adic_valuation(c, p) for c in cyc]
    return f"matdiagonal({list(diag)})"


