def _representative_for(deg: int, sig: Tuple[int, int]) -> Optional[Tuple[int, ...]]:
    """Return a canonical irreducible representative for the given
    (degree, signature) class, or None if no representative is on file.
    """
    return _REPRESENTATIVE_IRR.get((deg, (int(sig[0]), int(sig[1]))))


def _has_rational_root(coeffs: Sequence[int]) -> bool:
    """Lightweight reducibility check: does ``f(x)`` have a rational
    root? By the rational root theorem, candidate roots are
    ``+/- p/q`` with ``p | f[0]`` and ``q | f[deg]``.
    """
    if not coeffs:
        return True
    if coeffs[0] == 0:
        return True  # x | f
    deg = len(coeffs) - 1
    c0 = abs(int(coeffs[0]))
    cn = abs(int(coeffs[-1]))
    if cn == 0:
        return True  # not a polynomial of declared degree

    def divisors(n: int) -> List[int]:
        return [d for d in range(1, n + 1) if n % d == 0]

    for p in divisors(c0):
        for q in divisors(cn):
            for s in (1, -1):
                # f(s*p/q) = sum c_i * (s*p)^i * q^(deg-i) / q^deg
                num = s * p
                den = q
                val = 0
                for i, c in enumerate(coeffs):
                    val += int(c) * (num ** i) * (den ** (deg - i))
                if val == 0:
                    return True
    return False


def classify_trace_field(
    min_poly: Sequence[int],
    sig: Tuple[int, int],
) -> int:
    """Assign a trace-field minimal polynomial to one of N_CLASSES.

    ``min_poly`` is the polynomial coefficients in ascending degree
    (so ``[c_0, c_1, ..., c_d]`` represents
    ``c_0 + c_1*x + ... + c_d*x^d``). ``sig = (r1, r2)`` is the number-
    field signature; r1 + 2*r2 must equal the degree.
    """
    deg = max(0, len(min_poly) - 1)
    r1, r2 = int(sig[0]), int(sig[1])
    if deg == 0 or deg == 1:
        return 0  # Q
    if deg == 2:
        if r2 == 0:
            return 1  # real quadratic
        return 2  # complex quadratic (r1=0, r2=1)
    if deg == 3:
        if r2 == 0:
            return 3  # totally real cubic
        return 4  # complex cubic (r1=1, r2=1)
    if deg == 4:
        return 5
    return 6  # degree >= 5


