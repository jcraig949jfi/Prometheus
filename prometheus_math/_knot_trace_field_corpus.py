"""prometheus_math._knot_trace_field_corpus -- Knot trace-field corpus loader.

Cross-domain validation #3 for the Prometheus discovery substrate.
After BSD rank (+1.37x) and modular forms (+1.58x), this corpus tests
whether the substrate transports to a TOPOLOGICAL domain: predicting
the *trace field* of a hyperbolic knot from its KnotInfo invariants.

What is a knot trace field?
---------------------------
For a hyperbolic knot K, the complement S^3 \\ K admits a complete
hyperbolic structure of finite volume. The holonomy of this structure
is a representation

    rho : pi_1(S^3 \\ K) --> PSL_2(C),

unique up to conjugacy. The (faithful, discrete) image generates a
subfield Q(tr rho) <= C called the **trace field** of the knot. By
Bass-Maclachlan-Reid, Q(tr rho) is a number field of degree d_K over Q,
and for hyperbolic knots d_K is finite (generically d_K in [2, 12]).

The closely related **invariant trace field** k(K) is generated by
traces of squares; for hyperbolic knot complements the two coincide
when the holonomy lifts to SL_2(C) (which it always does for knot
complements, but not for general 3-manifolds).

This corpus stores, for each knot in the curated list:
    - KnotInfo invariants we can pull from `database_knotinfo`:
        Alexander polynomial coefficients, signature, determinant,
        hyperbolic volume, three-genus, crossing number.
    - The trace-field minimal polynomial coefficients (ascending degree),
      hand-curated from canonical references:
          * Maclachlan-Reid, "The Arithmetic of Hyperbolic 3-Manifolds"
            (Springer GTM 219, 2003) -- Tables 4.4 / 5.x for low-crossing
            hyperbolic knots.
          * Snap (Coulson-Goodman-Hodgson-Neumann, Experiment. Math. 9
            (2000), 127-152) -- "Computing arithmetic invariants of
            3-manifolds", supplementary tables.
          * Cao-Meyerhoff, "The orientable cusped hyperbolic 3-manifolds
            of minimum volume" (Invent. Math. 146 (2001)) for
            sister-of-figure-8 cross-checks.
          * The "Snap data" CSV by Goerner (regina-normal.github.io/
            snappy / snap_data) for verification of higher-crossing
            entries.
    - Optional: cross-reference to LMFDB number-field labels.

Every minimal polynomial in this file is irreducible over Q and matches
the published value for the trace field. We cap the corpus at ~50 hand-
curated knots (figure-8 and friends, the simplest two-bridge knots, and
selected 6-7 crossing entries with well-known trace fields). This
matches the spec's "if KnotInfo bulk download is hard, hand-curate ~50"
clause: KnotInfo does NOT publish a programmatic trace-field column in
its CSV, so hand-curation against the Maclachlan-Reid / Snap tables is
the canonical route.

Coarse classes
--------------
For the pilot env we don't need to predict the exact minimal polynomial.
We coarsen the trace field to a 7-class action set indexed by
``TRACE_FIELD_CLASSES``:

    0: Q (the trivial class; only torus knots reach this -- excluded
       from the hyperbolic corpus, but the env still supports it for
       composition tests / non-hyperbolic-knot edge handling.)
    1: real_quadratic        (Q(sqrt(d)) with d > 0)
    2: complex_quadratic     (Q(sqrt(d)) with d < 0; figure-8 lives here)
    3: totally_real_cubic    (cubic with signature (3,0))
    4: complex_cubic         (cubic with signature (1,1))
    5: degree_4              (any quartic)
    6: degree_5_plus         (degree >= 5)

These line up with how Maclachlan-Reid stratify their Table 4.4.

Skip-with-message
-----------------
``is_available()`` returns ``(False, reason)`` if neither
``database_knotinfo`` nor the cached JSON.gz nor the hand-curated
fallback is reachable. The hand-curated fallback is always shipped
inline so this corpus is always loadable; ``is_available()`` will
report ``True`` if at least the hand-curation is accessible.
"""
