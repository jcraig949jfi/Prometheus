def load_knot_trace_field_corpus(
    *,
    include_non_hyperbolic: bool = False,
    use_cache: bool = True,
    write_cache_after_build: bool = True,
    alexander_pad_to: int = DEFAULT_ALEXANDER_LEN,
) -> List[KnotTraceFieldEntry]:
    """Build / load the knot trace-field corpus.

    Order of preference:
      1. If cache exists and ``use_cache=True``: load it.
      2. Else build from ``_CURATED_TRACE_FIELDS`` joined with KnotInfo
         (when ``database_knotinfo`` is importable) and write the cache.
      3. Knots with no KnotInfo metadata are still emitted, but with
         volume / signature / determinant set to defaults derivable from
         the curated table.

    Parameters
    ----------
    include_non_hyperbolic : bool
        If True, include the (3,1), (8,19), (9,1), (10,124) torus knots
        that we list with placeholder Class 0 trace field. Default False
        because the env's main job is hyperbolic-knot prediction.
    use_cache : bool
        Honor on-disk cache.
    write_cache_after_build : bool
        Persist the corpus after a fresh build.
    alexander_pad_to : int
        Target length for zero-padded Alexander polynomial features.
        Curated/KnotInfo coefficients are right-padded with zeros up to
        this length.

    Returns
    -------
    list[KnotTraceFieldEntry]
    """
    global _LAST_LOAD_SOURCE

    if alexander_pad_to <= 0:
        raise ValueError(
            f"alexander_pad_to must be > 0; got {alexander_pad_to}"
        )

    cp = cache_path()
    pool: List[KnotTraceFieldEntry] = []
    if use_cache and cp.is_file() and cp.stat().st_size > 0:
        try:
            pool = read_cache(cp)
            _LAST_LOAD_SOURCE = f"cache:{cp}"
        except Exception:
            pool = []

    if not pool:
        # Build from curated + KnotInfo.
        ki_used = False
        for d in _CURATED_TRACE_FIELDS:
            name = d["name"]
            curated_poly = tuple(int(c) for c in d["trace_field_min_poly"])
            sig_pair = (int(d["signature"][0]), int(d["signature"][1]))
            if len(sig_pair) != 2:
                raise ValueError(
                    f"signature for {name} must be (r1, r2); got {sig_pair}"
                )
            curated_deg = max(0, len(curated_poly) - 1)
            curated_source = str(d.get("source", ""))

            # Verify irreducibility of the curated polynomial. If it
            # reduces, fall back to the (degree, signature)-indexed
            # representative; this preserves the env's class assignment
            # while ensuring every polynomial we ship is irreducible
            # over Q. The env task depends on (degree, signature) only,
            # so the substitution does not change the classification
            # ground truth.
            if curated_deg <= 1:
                # Placeholder Q row -- accept as-is.
                min_poly = curated_poly
                source = curated_source
            elif not _has_rational_root(curated_poly) and abs(int(curated_poly[-1])) == 1:
                # Passes the cheap RRT-based irreducibility necessary
                # condition; trust the curated polynomial.
                min_poly = curated_poly
                source = curated_source
            else:
                rep = _representative_for(curated_deg, sig_pair)
                if rep is not None:
                    min_poly = rep
                    source = (
                        f"{curated_source}|substituted-irreducible-rep"
                    )
                else:
                    # No representative on file for this (deg, sig);
                    # last-ditch fall back to the curated entry but
                    # log via source. Tests will catch this if it
                    # actually slips through.
                    min_poly = curated_poly
                    source = (
                        f"{curated_source}|FALLBACK-no-representative"
                    )
            cls = classify_trace_field(min_poly, sig_pair)

            ki = _knotinfo_lookup(name)
            if ki is not None:
                ki_used = True
                # Re-parse Alexander vector from KnotInfo (cleaner than the
                # already-shaped 'alexander_coeffs' field which omits zeros).
                alex_raw = None
                # The shaped knotinfo dict has 'alexander_coeffs' already.
                alex_coeffs = tuple(ki.get("alexander_coeffs") or ())
                if not alex_coeffs:
                    # Fall back to raw vector if shape didn't expose it.
                    alex_coeffs = tuple()
                crossings = int(ki.get("crossing_number") or 0)
                signature = int(ki.get("signature") or 0)
                determinant = int(ki.get("determinant") or 1)
                three_genus = int(ki.get("three_genus") or 0)
                vol = ki.get("hyperbolic_volume")
                hyp_vol = float(vol) if vol is not None else 0.0
            else:
                alex_coeffs = tuple()
                crossings = 0
                signature = 0
                determinant = 1
                three_genus = 0
                hyp_vol = 0.0

            # Pad / truncate the Alexander polynomial coefficient vector.
            if len(alex_coeffs) > alexander_pad_to:
                alex_coeffs = alex_coeffs[:alexander_pad_to]
            elif len(alex_coeffs) < alexander_pad_to:
                alex_coeffs = alex_coeffs + (0,) * (
                    alexander_pad_to - len(alex_coeffs)
                )

            entry = KnotTraceFieldEntry(
                name=name,
                crossing_number=crossings,
                signature=signature,
                determinant=determinant,
                three_genus=three_genus,
                hyperbolic_volume=hyp_vol,
                alexander_coeffs=tuple(int(c) for c in alex_coeffs),
                trace_field_min_poly=tuple(int(c) for c in min_poly),
                trace_field_signature=(int(sig_pair[0]), int(sig_pair[1])),
                trace_field_class=cls,
                nf_label=d.get("nf_label"),
                nf_discriminant=d.get("nf_discriminant"),
                nf_class_number=d.get("nf_class_number"),
                source=source,
            )
            pool.append(entry)

        if ki_used:
            _LAST_LOAD_SOURCE = "curated+knotinfo"
        else:
            _LAST_LOAD_SOURCE = "curated-only"

        if write_cache_after_build and pool:
            try:
                write_cache(pool, cp)
            except Exception:
                pass

    # Filter non-hyperbolic if requested.
    if not include_non_hyperbolic:
        pool = [e for e in pool if e.hyperbolic_volume > 0.0
                or e.trace_field_class != 0]
        # Note: the second clause keeps any curated row with cls != 0
        # even if KnotInfo did not return a volume (defensive). For our
        # current curation, cls=0 entries are exactly the torus knots.

    return list(pool)


def split_train_test(
    corpus: Sequence[KnotTraceFieldEntry],
    train_frac: float = 0.7,
    seed: int = 0,
) -> Tuple[List[KnotTraceFieldEntry], List[KnotTraceFieldEntry]]:
    """Reproducible train/test split over the corpus."""
    if not corpus:
        raise ValueError("cannot split an empty corpus")
    if not 0.0 < train_frac < 1.0:
        raise ValueError(f"train_frac must be in (0,1); got {train_frac}")
    import random as _random
    rng = _random.Random(seed)
    idx = list(range(len(corpus)))
    rng.shuffle(idx)
    n_train = int(round(len(corpus) * train_frac))
    if n_train <= 0:
        n_train = 1
    if n_train >= len(corpus):
        n_train = len(corpus) - 1
    train = [corpus[i] for i in idx[:n_train]]
    test = [corpus[i] for i in idx[n_train:]]
    return train, test


def corpus_summary(corpus: Iterable[KnotTraceFieldEntry]) -> dict:
    """Diagnostic counts: total + per-class + crossing-number range."""
    corpus = list(corpus)
    by_class: dict[str, int] = {c: 0 for c in TRACE_FIELD_CLASSES}
    crossings: list[int] = []
    degrees: list[int] = []
    volumes: list[float] = []
    for e in corpus:
        by_class[TRACE_FIELD_CLASSES[e.trace_field_class]] = (
            by_class[TRACE_FIELD_CLASSES[e.trace_field_class]] + 1
        )
        crossings.append(e.crossing_number)
        degrees.append(e.trace_field_degree)
        volumes.append(e.hyperbolic_volume)
    return {
        "n_total": len(corpus),
        "by_class": by_class,
        "crossing_min": min(crossings) if crossings else None,
        "crossing_max": max(crossings) if crossings else None,
        "trace_field_degree_min": min(degrees) if degrees else None,
        "trace_field_degree_max": max(degrees) if degrees else None,
        "hyperbolic_volume_min": min(volumes) if volumes else None,
        "hyperbolic_volume_max": max(volumes) if volumes else None,
        "source": last_load_source(),
    }


