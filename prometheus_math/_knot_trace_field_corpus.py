def _network_disabled() -> bool:
    """Tests can disable the network with PROMETHEUS_NO_NETWORK=1."""
    return bool(os.environ.get("PROMETHEUS_NO_NETWORK", "").strip())


def is_available(timeout: float = 5.0) -> Tuple[bool, str]:
    """Cheap reachability check.

    The hand-curated table is shipped inline so this corpus is ALWAYS
    available, regardless of network. We still report which source is
    active for diagnostic transparency.
    """
    cp = cache_path()
    if cp.is_file() and cp.stat().st_size > 0:
        return True, f"cache present at {cp}"
    # Always loadable -- hand-curated fallback is in-process.
    return True, "ok (hand-curated fallback)"


def _alex_from_knotinfo_vector(s: Any) -> Tuple[int, ...]:
    """Parse KnotInfo's alexander_polynomial_vector to coefficient tuple.

    Format: ``[lo_degree, length, c_lo, c_lo+1, ..., c_hi]``.
    Returns an ascending-degree coefficient tuple ``(c_lo, ..., c_hi)``,
    or empty tuple on parse failure.
    """
    if s is None:
        return tuple()
    txt = str(s).strip()
    if txt.startswith("["):
        txt = txt[1:]
    if txt.endswith("]"):
        txt = txt[:-1]
    parts = [p.strip() for p in txt.split(",") if p.strip()]
    if len(parts) < 3:
        return tuple()
    try:
        nums = [int(p) for p in parts]
    except ValueError:
        return tuple()
    return tuple(nums[2:])


def _knotinfo_lookup(name: str) -> Optional[dict]:
    """Pull KnotInfo invariants for a knot. Returns dict or None.

    Falls through to None silently when ``database_knotinfo`` is
    unavailable. Used to fill in basic invariants (volume, signature,
    Alexander polynomial) for the curated trace-field entries.
    """
    try:
        from .databases import knotinfo as _ki
    except Exception:
        return None
    try:
        rec = _ki.lookup(name)
    except Exception:
        return None
    return rec


