# Modular transformations (T: τ → τ+1, S: τ → -1/τ)
# ---------------------------------------------------------------------------

def theta_modular_transformation(n: int, z, tau, prec: Optional[int] = None):
    """Compute θ_n at the two generators of the modular group action.

    Returns the pair ``(theta_T, theta_S)`` where:

      * ``theta_T = θ_n(z, q')`` with ``q' = exp(iπ(τ+1))``
        (the T transform, τ → τ+1).
      * ``theta_S = θ_n(z/τ, q'')`` with ``q'' = exp(iπ · (-1/τ))``
        (the S transform, τ → -1/τ).

    These two values feed the standard θ-series modular consistency
    tests (cf. Diamond & Shurman, *A First Course in Modular Forms*,
    §1.2). The full modular transformation law adds an explicit
    multiplier of ``sqrt(-iτ) · exp(iπ z² / τ)`` to ``theta_S`` — we
    return the *raw* θ_n value at the new argument, leaving any
    multiplier to the caller (it depends on the consistency rule
    being tested).

    Parameters
    ----------
    n : int
        Theta index in {1,2,3,4}.
    z : numeric
        Argument.
    tau : numeric
        Modular parameter with Im(τ) > 0.
    prec : int, optional
        Working precision in bits.

    Returns
    -------
    (mpc, mpc)
        ``(theta_T, theta_S)``.

    Raises
    ------
    ValueError
        If Im(τ) ≤ 0.
    """
    _validate_n(n)
    p = _resolve_prec(prec)
    with workprec(p):
        tauc = _mpc(tau)
        if mpmath.im(tauc) <= 0:
            raise ValueError(
                f"modular transformation: Im(tau) must be > 0, got {mpmath.im(tauc)}"
            )
        zc = _coerce_z(z)

        # T transform: tau -> tau + 1, so q = exp(i pi (tau+1)) = -q_orig
        q_T = mpmath.exp(_mpc(0, 1) * mpmath.pi * (tauc + 1))
        theta_T = mpmath.jtheta(n, zc, q_T)

        # S transform: tau -> -1/tau, z -> z/tau
        tau_S = -1 / tauc
        q_S = mpmath.exp(_mpc(0, 1) * mpmath.pi * tau_S)
        z_S = zc / tauc
        theta_S = mpmath.jtheta(n, z_S, q_S)

        return theta_T, theta_S
