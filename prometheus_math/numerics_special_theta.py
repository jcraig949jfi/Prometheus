def lattice_theta_series(
    lattice_basis,
    z,
    max_terms: int = 8,
    prec: Optional[int] = None,
):
    """Theta series of a real lattice Λ ⊂ ℝ^d:

        θ_Λ(z) = Σ_{v ∈ Λ} exp(-π v · v + 2π i z · v).

    The lattice is generated by integer combinations of the columns of
    ``lattice_basis``; we sum over coefficient vectors in the box
    ``|m_i| ≤ max_terms``. For unimodular / well-conditioned bases this
    converges quickly because the integrand decays as
    ``exp(-π λ_min(B^T B) · ||m||²)``.

    Parameters
    ----------
    lattice_basis : Sequence[Sequence[float]] | mpmath.matrix
        d×k real matrix; columns are lattice basis vectors. ``d`` is the
        ambient dimension, ``k`` the lattice rank (k ≤ d).
    z : Sequence[numeric]
        d-vector (real or complex) — the argument of the theta series.
    max_terms : int, default 8
        Coefficient-box truncation.
    prec : int, optional
        Working precision in bits.

    Returns
    -------
    mpc
        θ_Λ(z) at the requested precision.

    Notes
    -----
    For the standard square lattice ℤ² (basis = I_2) and z = 0 this
    returns θ_3(0, e^{-π})² (Conway & Sloane, *Sphere Packings*, §4.1).
    """
    if not isinstance(max_terms, int) or max_terms <= 0:
        raise ValueError(f"max_terms must be a positive int, got {max_terms!r}")

    p = _resolve_prec(prec)
    with workprec(p):
        # Coerce basis to list-of-lists (rows × cols = d × k).
        if hasattr(lattice_basis, "rows") and hasattr(lattice_basis, "cols"):
            d = lattice_basis.rows
            k = lattice_basis.cols
            B = [[_mpf(lattice_basis[i, j]) for j in range(k)] for i in range(d)]
        else:
            rows = [list(r) for r in lattice_basis]
            d = len(rows)
            if d == 0:
                raise ValueError("lattice_theta_series: basis must be non-empty")
            k = len(rows[0])
            if any(len(r) != k for r in rows):
                raise ValueError(
                    "lattice_theta_series: basis must be rectangular"
                )
            B = [[_mpf(rows[i][j]) for j in range(k)] for i in range(d)]

        try:
            zlist = list(z)
        except TypeError as e:
            raise ValueError(f"lattice_theta_series: cannot interpret z: {e}")
        if len(zlist) != d:
            raise ValueError(
                f"lattice_theta_series: z has length {len(zlist)} but "
                f"basis has {d} rows"
            )
        zvec = [_mpc(v) for v in zlist]

        pi = mpmath.pi
        two_pi_i = 2 * pi * _mpc(0, 1)

        total = _mpc(0)
        rng = range(-max_terms, max_terms + 1)
        for m_tuple in itertools.product(rng, repeat=k):
            # v = B @ m
            v = [_mpf(0)] * d
            for i in range(d):
                acc = _mpf(0)
                for j in range(k):
                    if m_tuple[j] != 0:
                        acc += B[i][j] * _mpf(m_tuple[j])
                v[i] = acc
            # v · v (real)
            vv = _mpf(0)
            for i in range(d):
                vv += v[i] * v[i]
            # z · v (complex)
            zv = _mpc(0)
            for i in range(d):
                zv += zvec[i] * v[i]
            total += mpmath.exp(-pi * vv + two_pi_i * zv)
        return total


