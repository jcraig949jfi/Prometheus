def jacobi_theta(n: int, z, q, prec: Optional[int] = None):
    """Jacobi theta function θ_n(z, q) for n ∈ {1, 2, 3, 4}.

    Parameters
    ----------
    n : int
        Theta index, one of {1, 2, 3, 4}.
    z : numeric
        Argument; real, complex, or mpmath value.
    q : numeric
        Nome with |q| < 1. The series only converges in the open
        unit disc.
    prec : int, optional
        Working precision in BITS. Defaults to ambient ``mp.prec``.

    Returns
    -------
    mpc
        θ_n(z, q) as an mpmath complex number.

    Raises
    ------
    ValueError
        If ``n`` is not in {1,2,3,4} or if ``|q| ≥ 1``.

    Notes
    -----
    Backend: ``mpmath.jtheta(n, z, q)``. mpmath uses an adaptive series
    truncation that achieves the requested precision automatically.
    """
    _validate_n(n)
    p = _resolve_prec(prec)
    with workprec(p):
        zc = _coerce_z(z)
        qc = _validate_q(q)
        return mpmath.jtheta(n, zc, qc)


def jacobi_theta_derivative(n: int, z, q, prec: Optional[int] = None):
    """First derivative ∂/∂z θ_n(z, q).

    Used for example in the Jacobi identity

        θ_1'(0, q) = θ_2(0, q) · θ_3(0, q) · θ_4(0, q)

    (Whittaker & Watson §21.41).

    Parameters
    ----------
    n : int
        Theta index in {1,2,3,4}.
    z, q : numeric
        Argument and nome (|q| < 1).
    prec : int, optional
        Working precision in bits.

    Returns
    -------
    mpc
        ∂/∂z θ_n(z, q).
    """
    _validate_n(n)
    p = _resolve_prec(prec)
    with workprec(p):
        zc = _coerce_z(z)
        qc = _validate_q(q)
        # mpmath.jtheta accepts an optional 4th positional `derivative`
        return mpmath.jtheta(n, zc, qc, 1)


def theta_null_value(n: int, q, prec: Optional[int] = None):
    """θ_n(0, q) — the *theta nullwert* (Jacobi's special evaluation).

    These special values appear all over modular-form theory:

      * θ_2(0, q), θ_3(0, q), θ_4(0, q) generate the modular forms
        for Γ_0(2) on H.
      * θ_3(0, q)^2 = 2K(k)/π for the Legendre modulus k that solves
        k = θ_2(0, q)^2 / θ_3(0, q)^2.
      * θ_2(0, q) = 0 *only* for q = 0 — it is otherwise non-vanishing
        in the disc.

    Note θ_1(0, q) ≡ 0 identically (θ_1 is odd in z).

    Parameters
    ----------
    n : int
        Theta index in {1,2,3,4}.
    q : numeric
        Nome with |q| < 1.
    prec : int, optional
        Working precision in bits.

    Returns
    -------
    mpc
        θ_n(0, q).
    """
    return jacobi_theta(n, 0, q, prec=prec)


# ---------------------------------------------------------------------------
# Riemann theta function on the Siegel upper half-space
# ---------------------------------------------------------------------------

def _validate_omega(Omega) -> Tuple[list, int]:
    """Validate a Siegel period matrix Ω.

    Requirements:
      * Square g×g matrix (list of lists, mpmath.matrix, etc.)
      * Symmetric: Ω = Ω^T (within ambient tolerance)
      * Imaginary part is positive-definite (so ``Σ exp(πi n^T Ω n …)``
        converges).

    Returns
    -------
    (rows, g) : tuple
        Row-major list-of-lists representation and dimension g.
    """
    # Accept mpmath.matrix or list-of-lists / numpy ndarray.
    try:
        if hasattr(Omega, "rows") and hasattr(Omega, "cols"):
            g = Omega.rows
            if Omega.cols != g:
                raise ValueError(
                    f"Riemann theta: Omega must be square, got {Omega.rows}x{Omega.cols}"
                )
            rows = [[Omega[i, j] for j in range(g)] for i in range(g)]
        else:
            rows = [list(r) for r in Omega]
            g = len(rows)
            if g == 0:
                raise ValueError("Riemann theta: Omega must be non-empty")
            if any(len(r) != g for r in rows):
                raise ValueError(
                    "Riemann theta: Omega must be square; got non-uniform shape"
                )
    except (TypeError, ValueError) as e:
        raise ValueError(f"Riemann theta: cannot interpret Omega: {e}")

    # Coerce all entries to mpc.
    rows = [[_mpc(rows[i][j]) for j in range(g)] for i in range(g)]

    # Symmetry check.
    eps_sym = mpmath.mpf(2) ** (-mp.prec + 8)
    for i in range(g):
        for j in range(i + 1, g):
            if abs(rows[i][j] - rows[j][i]) > eps_sym:
                raise ValueError(
                    f"Riemann theta: Omega must be symmetric; "
                    f"|Omega[{i},{j}] - Omega[{j},{i}]| = "
                    f"{abs(rows[i][j] - rows[j][i])}"
                )

    # Positive-definite imaginary part — for g <= 4 we test via principal
    # minor determinants (Sylvester's criterion). For larger g this is
    # still O(g^4) and acceptable at the precisions we operate.
    Y = [[mpmath.im(rows[i][j]) for j in range(g)] for i in range(g)]
    for k in range(1, g + 1):
        sub = [[Y[i][j] for j in range(k)] for i in range(k)]
        d = mpmath.det(mpmath.matrix(sub))
        if mpmath.re(d) <= 0:
            raise ValueError(
                f"Riemann theta: Im(Omega) must be positive-definite; "
                f"leading {k}x{k} minor has det={d}"
            )

    return rows, g


def _validate_z_vector(z, g: int) -> list:
    """Coerce z to a g-vector of mpc."""
    if hasattr(z, "rows") and hasattr(z, "cols"):
        if z.cols == 1:
            zlist = [z[i, 0] for i in range(z.rows)]
        elif z.rows == 1:
            zlist = [z[0, j] for j in range(z.cols)]
        else:
            raise ValueError(
                f"Riemann theta: z must be a g-vector, got matrix {z.rows}x{z.cols}"
            )
    else:
        try:
            zlist = list(z)
        except TypeError as e:
            raise ValueError(f"Riemann theta: cannot interpret z as vector: {e}")
    if len(zlist) != g:
        raise ValueError(
            f"Riemann theta: z must have length g={g}, got length {len(zlist)}"
        )
    return [_mpc(v) for v in zlist]


def riemann_theta(z, Omega, max_terms: int = 8, prec: Optional[int] = None):
    """Riemann theta function θ(z, Ω).

        θ(z, Ω) = Σ_{n ∈ ℤ^g} exp(π i n^T Ω n + 2π i n^T z)

    The infinite lattice sum is truncated to the box ``|n_i| ≤ max_terms``
    in each coordinate, so the cost is ``O((2·max_terms+1)^g)``.

    For convergence, Ω must lie in the Siegel upper half-space (symmetric
    with positive-definite imaginary part). The summand decays as
    ``exp(-π Im(Ω)_min · ||n||²)``, so a small ``max_terms`` is usually
    enough; the user should increase ``max_terms`` for Ω close to the
    real axis.

    Parameters
    ----------
    z : Sequence[numeric] | mpmath.matrix
        g-vector argument.
    Omega : Sequence[Sequence[numeric]] | mpmath.matrix
        g×g period matrix in Siegel upper half-space.
    max_terms : int, default 8
        Box truncation per coordinate. Sum has (2·max_terms+1)^g terms.
    prec : int, optional
        Working precision in bits.

    Returns
    -------
    mpc
        θ(z, Ω) at the requested precision.

    Raises
    ------
    ValueError
        If Omega is non-square / non-symmetric / has non-positive
        imaginary part, or z has wrong length, or max_terms ≤ 0.
    """
    if not isinstance(max_terms, int) or max_terms <= 0:
        raise ValueError(f"max_terms must be a positive int, got {max_terms!r}")

    p = _resolve_prec(prec)
    with workprec(p):
        rows, g = _validate_omega(Omega)
        zvec = _validate_z_vector(z, g)

        pi_i = mpmath.pi * _mpc(0, 1)
        two_pi_i = 2 * pi_i

        total = _mpc(0)
        rng = range(-max_terms, max_terms + 1)
        for n_tuple in itertools.product(rng, repeat=g):
            # n^T Ω n
            quad = _mpc(0)
            for i in range(g):
                ni = n_tuple[i]
                if ni == 0:
                    continue
                for j in range(g):
                    nj = n_tuple[j]
                    if nj == 0:
                        continue
                    quad += _mpf(ni) * rows[i][j] * _mpf(nj)
            # n^T z
            lin = _mpc(0)
            for i in range(g):
                ni = n_tuple[i]
                if ni == 0:
                    continue
                lin += _mpf(ni) * zvec[i]
            total += mpmath.exp(pi_i * quad + two_pi_i * lin)
        return total


