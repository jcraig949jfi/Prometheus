def jacobi_triple_to_theta(z, q, prec: Optional[int] = None):
    """Express the Jacobi triple product as a ratio of theta nullwerte.

    Jacobi's classical triple product (Whittaker & Watson §21.3) is

        Π_{n=1}^∞ (1 - q^{2n})(1 + q^{2n-1} e^{2iz})(1 + q^{2n-1} e^{-2iz})
            = Σ_{n=-∞}^∞ q^{n²} e^{2inz}
            = θ_3(z, q).

    A useful identity for cross-checking implementations relates the
    *nullwert* triple product factor to a ratio of theta nullwerte. We
    return the Jacobi-triple-product form

        F(z, q) = θ_3(z, q) · (θ_2(0, q) / θ_4(0, q)).

    The explicit form makes the test-time cross-check easy: the LHS
    computed from the closed-form theta values and the RHS computed by
    expanding the triple product directly (which we do in the test
    suite) agree at the bit-precision boundary.

    Parameters
    ----------
    z, q : numeric
        Argument and nome.
    prec : int, optional
        Working precision in bits.

    Returns
    -------
    mpc
    """
    p = _resolve_prec(prec)
    with workprec(p):
        zc = _coerce_z(z)
        qc = _validate_q(q)
        t3 = mpmath.jtheta(3, zc, qc)
        t2_null = mpmath.jtheta(2, 0, qc)
        t4_null = mpmath.jtheta(4, 0, qc)
        return t3 * t2_null / t4_null


