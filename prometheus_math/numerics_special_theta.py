# ---------------------------------------------------------------------------
# Validation helpers
# ---------------------------------------------------------------------------

def _validate_n(n: int) -> int:
    """Ensure n is one of {1, 2, 3, 4} (the four Jacobi theta indices)."""
    if not isinstance(n, (int,)):
        raise ValueError(
            f"theta index n must be an int in {{1,2,3,4}}, got {type(n).__name__}"
        )
    if n not in (1, 2, 3, 4):
        raise ValueError(
            f"theta index n must be in {{1,2,3,4}}, got {n}"
        )
    return n


def _validate_q(q) -> _mpc:
    """Coerce q to mpc and verify |q| < 1.

    The nome q lives strictly inside the open unit disc; the series
    diverges on the boundary. Integer / float inputs are auto-promoted
    to mpc so the rest of the pipeline can stay typed.
    """
    qc = _mpc(q)
    if abs(qc) >= 1:
        raise ValueError(
            f"nome q must satisfy |q| < 1 (open unit disc); got |q|={abs(qc)}"
        )
    return qc


def _coerce_z(z) -> _mpc:
    """Coerce a scalar z (int/float/complex/mpc) to mpc."""
    return _mpc(z)


def _resolve_prec(prec: Optional[int]) -> int:
    """Return the working precision in bits.

    If ``prec`` is None, fall back to ``mp.prec`` (the ambient mpmath
    precision). Otherwise use the explicit value.
    """
    if prec is None:
        return mp.prec
    if not isinstance(prec, int) or prec <= 0:
        raise ValueError(f"prec must be a positive int, got {prec!r}")
    return prec


